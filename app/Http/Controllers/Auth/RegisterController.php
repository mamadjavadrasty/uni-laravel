    {
        $inputs = $request->all();

        $inputs['request_dormitory'] = isset($inputs['request_dormitory']) ? 1:0;

        $create = Student::create($inputs);

        if ($create){
            return response()->json(['success'=>['message'=>'ثبت نام با موفقیت انجام شد']]);
        }

        return response()->json(['error'=>['message'=>'خطایی رخ داده است دوباره تلاش کنید']]);
